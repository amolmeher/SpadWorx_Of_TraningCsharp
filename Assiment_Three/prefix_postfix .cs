using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Assiment_Three
{
    class prefix_postfix
    {
        static void Main(string[] args)
        {
            var x = 42;
            Console.WriteLine(++x); // 43
            Console.WriteLine(x++);//43
            Console.WriteLine(--x);//43
            Console.WriteLine(x--);//43

        }
    }
}
