/*
  Sealed Class: Declared with Sealed keyword, 
which enables this class to seal all its variables,
methods and properties.
No other class can inherit anything from this class or in other words,
this class cannot be inherited. But we can instantiate this class,
i.e we can have any number of objects of a sealed class.



Abstract Class: Declared with abstract keyword, 
this class is primarily created as a Inheritable class.
An abstract class enables other classes to inherit from this class,
but forbids to instantiate. 
One can inherit from an abstract class
but we cannot create objects of an abstract class.
Abstract class can have abstract as well as non abstract methods.
Abstract methods are those which are not having method definition.
 */